                ဒေသအထွက် ကောင်းမျိုးများကို အသုံးပြုပါ ။ (၁) ဧက အပင်ဦးရေ တစ်သိန်းသုံးသောင်း ရရှိရန် ပင်ကြား (၄)လက်မ ၊ တန်းကြား (၁၂) လက်မ ထား၍ မျိုးစေ့ (၁)တင်းထိ အသုံးပြုနိုင်ပါသည် ။
                မျိုးစေ့ကို မျိုးစေ့လူးနယ်ဆေး တစ်မျိုးမျိုး နှင့် လူးနယ်ပါ။
              </p><br>
              </p>

              <p class="pt-3"><b>၈။စိုက်ပျိုးခြင်း</b></p>
              <p>
                (၀၁ ရက်) <br>

                ကုလားပဲမျိုးစေ့ကိုပဲမြစ်ဖုမြေဩဇာတစ်ထုတ်နှင့်သမအောင်လူးနယ်စိုက်ပါ။အပင်ပေါက်ကောင်း၍အပင်ပေါက်ညီညာရန်ထွန်ကြောင်းအတွင်းမျိုးစေ့အတိမ်အနက် (၂) လက်မ မှ (၄) လက်မ အတွင်း ရအောင်စိုက်ပါ။
                <br>
                (၀၅-၁၅ ရက်)<br>

                အပင်ပေါက်စုံပါသည်။ အပင်ငယ်စဉ်လှေးကျိုင်း၊ပျ၊မွှားပင့်ကူနီ၊ရွက်စားပိုး(ငမြောင်တောင်)စသည့်ပိုးမွှားများကို ကာကွယ်ရန် ပင်လုံးစိမ့်ပြန့်သတ္တိရှိ သော ဆေးတစ်မျိုးမျိုးကို တစ်ဧကသတ်မှတ်နှုန်းထားအတိုင်းပတ်ဖျန်းပါ။ပေါင်းမြက်ကင်းစင်အောင်စတင်ဆောင်ရွက်ပါ။(၁၅)ရက်ခြား(၂)ကြိမ်ဆောင်ရွက်ပါ။
                <br>
                (၁၅ရက်-၃၀ရက်) <br>

                ပဲပင်များကြီးထွားသန်မာဖြစ်ထွန်းရန်(NPK)နှင့်အနည်းလိုအာဟာရဓါတ်များပါဝင်သောရွက်ဖျန်းမြေဩဇာများတွင်(N)အချိုးများသည်သင့်တော်သောရွက်ဖျန်းမြေဩဇာတစ်မျိုး မျိုး ကို(၁၅)ရက်ခြား၍နှစ်ကြိမ်ပက်ဖျန်းပါ။
                <br>
                (၃၅ရက်-၄၅ရက်)<br>

                ပန်းပွင့်ချိန်စသည်။အစိုဓာတ်မပျက်ရန်နှင့်အာဟာရဓာတ်မပျက်ရန် အရေးကြီးသည်။ ရက် (၄၀) ခန့် ပန်းဖြိုင်ဖြိုင်ပွင့်သည်။
                <br>
                (၁၅ရက်-၆၀ရက်)<br>

                ပွင့်အားကောင်း၍ အသီးများရန် NPK အချိုးရွက်ဖျန်းမြေဩဇာများ တွင် P နှင့် K အချိုး များသည့် ရွက်ဖျန်းမြေဩဇာ (၁၅) ရက်ခြား နှစ်ကြိမ်ဖျန်းပါ။ (သို့) ယူရီးယား (၄) ဇွန်းနှင့် လက်ချား (၁.၅) ဇွန်း၊ ရေတစ်ဂါလံနှင့် ရောပြီး တစ်ဧက (၂၀) ဂါလံဖျန်းပါ။ ရွက်ဖောက် လှေးကျိုင်း၊ ရွက်စားငမြောင်တောင်၊ စုပ်စားပိုး၊ ပျသရစ်၊ ထွာကောင် အစိမ်း၊ ရွက်စုံစားခူဝါ၊ သီးလုံးဖောက်ပိုးစသည်တို့မကျရောက်စေရန် (၁၅) ရက်သား မှ (၆၀) ရက်သားအထိ (၁၅) ရက်တစ်ကြိမ် ပင်လုံး ပြန့်ပိုးသတ်ဆေး တစ်မျိုးမျိုးအလှည့်ကျ ပတ်ဖျန်းပါ။
                <br>
                (၆၀ရက်-၈၀ရက်)<br>

                သီးနှံသက်တမ်းတစ်လျှောက် ကျရောက်တက်သော အပင်ညိုးရော ဂါ၊ မြစ်ခြောက်ဆွေးရောဂါ၊ ပင်ခြေပုတ်ရောဂါများကို ကာကွယ်ရန် မှိုသတ်ဆေးတစ်မျိုးမျိုးကို (၁၅) ရက်တစ်ကြိမ် အလှည့်ကျ ပတ်ဖျန်း ပါ။ အသီးနှင့်အစေ့အဆံ တည်ချိန်ဖြစ်၍ သီးလုံးဖောက်ပိုးကို ဂရုပြု ပါ။ ပဲစေ့လေးများ ပြည့်တင်းကြီးထွာချိန်ဖြစ်၍ တစ်ပင်လုံးချက်လုပ် သော အစာ၏ (၆၀) ရာခိုင်နှုန်းကို အစေ့အောင်မြင်ရန် အသုံးချရ၍ အစာအာဟာရ အလိုအပ်ဆုံးအချိန်ဖြစ်သည်။ အရွက်များစိမ်းစို ကျန်းမာနေရန်နှင့် အာဟာရအစိုဓာတ်မပျက်ရန် အရေးကြီးသည်။
              </p><br>
              </p>

              <p class="pt-3"><b>၉။ကျရောက်နိုင်သောရောဂါများ</b></p>
              <p>
                ဖားဥမှိုရောဂါ၊
                သံချေးရောဂါ၊
                ဒေါင်းနီးရွက်ခြောက်ရောဂါ၊
                ဘက်တီးရီးယားရွက်ခြောက်ရောဂါ။

              <p><b>ပိုးမွှားများ</b><br><br>

                လှေးပိုး၊ ယင်ဖြူ၊ ဖြုတ်ပိုး၊ ပဲပုပ်ပျပိုး၊၊

              </p><br>
              </p>

              <p class="pt-3"><b>၉။ရိတ်သိမ်းခြင်း</b></p>

              <p>

                ကုလားပဲမျိုး၊ ပတ်ဝန်းကျင်၊ ရာသီဥတုနှင့် ပြုစုစောင့်ရှောက်မှုပေါ် မူတည်၍ ရက် (၉၀-၁၂၀) အတွင်း ရင့်မှည့်လေ့ရှိကြသည်။ သီးတောင့်အရောင် ကောက်ရိုးရောင်၊ အညိုဖျော့ရောင်
                ပြောင်းသွားချိန် (သို့) ၈၀ ရာခိုင်နှုန်းသောသီးတောင့်များ ရင့်မှည့်သောအချိန်တွင်ရိတ်သိမ်းနိုင်သည်။ သီးတောင့်များအား နယ်လှေ့သန့်စင်ပြီး (၂ -၃) နေပြကာ အစိုဓာတ် (၈-၉) ရာခိုင်နှုန်းရှိချိန်တွင်
                သိုလှောင်ပါ။
              </p>
