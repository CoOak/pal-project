                သဲဆန်သောယာမြေတို့တွင် မိုးဦးသီးနှံများဖြစ်သော မြေပဲ၊ နှမ်း၊ ပြောင်းဖူးသီးနှံများအပြီးတွင် မြေကိုမှုန့်ညက်အောင် ထယ်ထိုးထွန်ယက်ပြီးနောက် ကြမ်းတုံးဖြင့်မြေညှိကာ
                တစ်တန်းနှင့် တစ်တန်း (၁၂)လက္မအကွာ ထွန်ကြောင်းများဆွဲရသည်။ ကိုင်းမြေ၊ ကျွန်းမြေများတွင်လည်း အလားတူပင် ဆောင်ရွက်ရသည်။ ကြောင်းဆွဲပြီးသောအခါ
                အစေ့ကြီးမျိုးကို တစ်ဧကလျှင် (၇၂)ပေါင်ဝင်(၁)တင်းနှုန်း၊ အစေ့သေးမျိုးကို (၈)ပြီနှုန်းဖြင့် ထွန်ကြောင်းများထဲသို့ လက်ဖြူးချပေးရသည်။ အကြမ်းအားဖြင့် တစ်စေ့နှင့်တစ်စေ့
                (၄)လက္မခန့်ကွာဝေးရသည်။ မျိုးစေ့ချပြီးသောအခါ ကြမ်းတုံးဖြင့် ဖုံးပေးရသည်။ စိုက်ပြီးသည့်နေ့မှ (၆-၈)ရက်ကြာလျှင် အပင်များစပေါက်သည်။ ကြောင်းဖြင့်မစိုက်ဘဲ
                ကြဲပက်စိုက်ပျိုးသည့်နည်းကိုလည်း အသုံးပြုကြသည်။ မြန်မာနိုင်ငံအထက်ပိုင်း ကိုင်းကျွန်းမြေများတွင် ကြောင်းဆွဲမစိုက်ဘဲ ကြဲပက်စိုက်ပျိုးကြသည်။ အပင်ပေါက်ပြီးနောက်
                သက်တမ်း(၅)လခန့်ကြာလျှင် အတောင့်များ ရင့်မှည့်ခြောက်သွေ့သည်။ အတောင့်ခြောက်များကို လက်ဖြင့်ခူးဆွတ်၍ အစေ့ထုတ်ယူသည်။ အပင်များကို နွားစာအဖြစ်ရိတ်သိမ်းသည်။
                စနစ်တကျ ထွန်ယက်စိုက်ပျိုးပါက တစ်ဧကလျှင် (၁၅)တင်းအထိ ထွက်နိုင်သည်။ မြန်မာနိုင်ငံတွင် (၆-၁၁)တင်းအထိ ထွက်သည်။

              </p>

              <p class="pt-3"><b>၆။ စိုက်ပျိုးခြင်း</b></p>
              <p>
                စားတော်ပဲသည် တစ်ရာသီသီးနှံဖြစ်သည်။ မြေအမျိုးအစားအမျိုးမျိုးတွင်စိုက်ပျိုးနိုင်ပြီး မစေးလွန်း မဖွယ်လွန်းသောမြေတွင် ကောင်းစွာဖြစ်ထွန်းသည်။
                နှစ်ရှည်ပေါင်းပင်များပေါက်ရောက်တတ်သောမြေတွင် စိုက်ပျိုး၍မကောင်းပါ။ နှံစားသီးနှံများ၏ အစိုဓာတ်လိုအပ်မှုနှင့်တူညီပြီး ဆားပေါက်ဒဏ်၊ ရေလွှမ်းဒဏ်ကိုခံနိုင်ရည်မရှိပါ။
                စားတော်ပဲသည် ကောင်းစွာမထွန်ယက်ထားသောမြေတွင်လည်း ဖြစ်ထွန်းသည်။ ဆောင်းဦးတွင်စိုက်ပျိုးမည်ဆိုပါက ပျိုးဘောင်များ ခြောက်သွေ့တာမျိုးမဖြစ်စေရန် မြေအလွန်အကျွံ
                ထွန်ယက်ခြင်းကို မပြုလုပ်သင့်ပါ။ အညှောင့်ဖောက်ရာတွင် နှံစားသီးနှံတွေထက် အစိုဓာတ်ပိုမိုလိုအပ်ပါသည်။ တန်းကြား ၆-၁၂ လက်မဖြင့် စိုက်ပျိုးနိုင်သည်။ စားတော်ပဲသည်
                နိုက်ထရိုဂျင်ပမာဏကို လေထုထဲကသော်လည်းကောင်း မြေကြီးထဲတွင်ရှိသော အကျိုးပြုဘက်တီးရီးယားများမှသော်လည်းကောင်း ဖမ်းယူနိုင်စွမ်းရှိသည်။ ပဲများတွင်
                နိုက်ထရိုဂျင်လိုအပ်မှုပမာဏသည် နိုက်ထရိုဂျင်ဖမ်းယူနိုင်တဲ့ပေါ်မှာ အမျိုးမျိုးကွဲပြားပါသည်။ ပင်ပိုင်းကြီးထွားမှုနောက်ပိုင်းနှင့် မျိုးပွားပိုင်းအစောပိုင်းအဆင့်တွင် အပူချိန်မြင့်မားပြီး
                မြေကြီးခြောက်သွေ့နေပါက နိုက်ထရိုဂျင်ဖမ်းယူနိုင်မှုတွင် ထိခိုက်နိုင်သည်။စတော်ပဲသည်နိုက်ထရိုဂျင်ဖမ်းယူမှုကောင်းသောသီးနှံဖြစ်ပြီး ကြီးထွားမှုကောင်းသောအခြေအနေတွင်
                နိုက်ထရိုဂျင်လိုအပ်မှုရဲ့ ၈၀ ရာခိုင်နှုန်းကို ဖမ်းယူခြင်းဖြင့် ရရှိသည်။ စားတော်ပဲမျိုးစေ့များကို ပဲမြစ်ဖုဇီဝမြေသြဇာဖြင့်လူးနယ်စိုက်ပျိုးဖို့လိုအပ်ပါသည်။ မျိုးစေ့များကို
                ပဲမြစ်ဖုဇီဝမြေသြဇာလူးနယ်ခြင်းသည် အရေးကြီးသည်။လူးနယ်စိုက်ပျိုးရာ၌ နည်းလမ်းမမှန်လျှင်လည်းနိုက်ထရိုဂျင်ဓာတ်ဖမ်းယူရရှိနိုင်မှုကျဆင်းတတ်သည်။လုပ်ငန်းစဉ်အောင်မြင်
                မမြင်ကိုအပင်ပေါက်ပြီးနှစ်ပတ်၊လေးပတ်တွင်အပင်အချို့ကိုသေချာစွာဆွဲနုတ်၍အမြစ်များတွင်မြစ်ဖုလေးများရှိမရှိစစ်ဆေးနိုင်ပါသည်။ ပန်းရောင် (သို့) အနီရောင်မြစ်ဖုလေးများတွယ်ကပ်နေပါက
                အောင်မြင်ပါသည်။ အကယ်၍ လူးနယ်စိုက်ပျိုးမှုမအောင်မြင်ခဲ့ပါက နိုက်ထရိုဂျင်ဓာတ်မြေသြဇာကျွေးပေးဖို့လိုပါမည်။

              </p>

              <p class="pt-3"><b>၇။ကျရောက်နိုင်သောရောဂါများ</b></p>
              <p>
                ရွက်ခြောက်ရောဂါ၊ အမြစ်ပုပ်ရောဂါ၊ ပင်စည်ပုပ်ရောဂါ၊ ဖားဥမှိုရောဂါများ ကျရောက်တတ်သည်။
              </p><br>
              <p><b>ပိုးမွှားများ</b><br><br>

                လှေးပိုး၊ ပဲဂျပိုး၊ ဖြုတ်စိမ်းများကျရောက်တတ်သည်။
              </p><br>
              </p>

              <p class="pt-3"><b>၈။ရိတ်သိမ်းခြင်း</b></p>

              <p>
                အထွက်နှုန်းကောင်းပြီးအရည်အသွေးကောင်းဖို့အတွက်သည်ရိတ်သိမ်းစနစ်တွင်လည်း မူတည်ပါသေးသည်။ စားတော်ပဲများကို ရိတ်သိမ်းရာတွင် အသီးတောင့်ထိပ်ပိုင်းစိမ်းနေပြီး
                အလယ်လောက်တွင် ဝါနေပြီဆိုပါက စတင်ရိတ်သိမ်းနိုင်သည်။ အခြောက်ခံရာတွင် အစေ့အစိုဓာတ် ၂၅-၃၀ ရာခိုင်နှုန်းသည် အကောင်းဆုံးဖြစ်သည့်အတွက် အစိုဓာတ်
                ၁၈-၂၀ရှိချိန်တွင်စတင်ရိတ်သိမ်းပြီးအခြောက်ခံဖို့ပုံထားသင့်သည်။ရိတ်သိမ်းသည့်အခါတွင်ပေါင်းမြက်များမပါဖို့သတိပြုရပါမည်။အားလုံးရင့်မှည့်ပြီးခြောက်မှရိတ်သိမ်းလျှင်အလေ့အလွှင့်များနိုင်သည့်အတွက်
                သီးတောင့်အစိမ်းရောင်မှ အဝါရောင်ပြောင်းစတွင်ရိတ်သိမ်းပြီးစုပုံ၍နေလှန်းထားသင့်သည်။မနက်စောစောပိုင်းနှင့်ညနေပိုင်းများတွင်ရိတ်သိမ်းနိုင်သည်။ထိုအချိန်များတွင်ရိတ်သိမ်းပါကအစိုဓာတ်များရှိနေသော
                ကြောင့် ကြွေဖို့ရာခိုင်နှုန်းနည်းစေပါသည်။
              </p>
