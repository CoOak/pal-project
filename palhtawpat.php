                သဲဆန်သောယာမြေတို့တွင် မိုးဦးသီးနှံများဖြစ်သော မြေပဲ၊ နှမ်း၊ ပြောင်းဖူးသီးနှံများအပြီးတွင် မြေကိုမှုန့်ညက်အောင် ထယ်ထိုးထွန်ယက်ပြီးနောက် ကြမ်းတုံးဖြင့်မြေညှိကာ
                တစ်တန်းနှင့် တစ်တန်း (၁၂)လက္မအကွာ ထွန်ကြောင်းများဆွဲရသည်။ ကိုင်းမြေ၊ ကျွန်းမြေများတွင်လည်း အလားတူပင် ဆောင်ရွက်ရသည်။ ကြောင်းဆွဲပြီးသောအခါ
                အစေ့ကြီးမျိုးကို တစ်ဧကလျှင် (၇၂)ပေါင်ဝင်(၁)တင်းနှုန်း၊ အစေ့သေးမျိုးကို (၈)ပြီနှုန်းဖြင့် ထွန်ကြောင်းများထဲသို့ လက်ဖြူးချပေးရသည်။ အကြမ်းအားဖြင့် တစ်စေ့နှင့်တစ်စေ့
                (၄)လက္မခန့်ကွာဝေးရသည်။ မျိုးစေ့ချပြီးသောအခါ ကြမ်းတုံးဖြင့် ဖုံးပေးရသည်။ စိုက်ပြီးသည့်နေ့မှ (၆-၈)ရက်ကြာလျှင် အပင်များစပေါက်သည်။ ကြောင်းဖြင့်မစိုက်ဘဲ
                ကြဲပက်စိုက်ပျိုးသည့်နည်းကိုလည်း အသုံးပြုကြသည်။ မြန်မာနိုင်ငံအထက်ပိုင်း ကိုင်းကျွန်းမြေများတွင် ကြောင်းဆွဲမစိုက်ဘဲ ကြဲပက်စိုက်ပျိုးကြသည်။ အပင်ပေါက်ပြီးနောက်
                သက်တမ်း(၅)လခန့်ကြာလျှင် အတောင့်များ ရင့်မှည့်ခြောက်သွေ့သည်။ အတောင့်ခြောက်များကို လက်ဖြင့်ခူးဆွတ်၍ အစေ့ထုတ်ယူသည်။ အပင်များကို နွားစာအဖြစ်ရိတ်သိမ်းသည်။
                စနစ်တကျ ထွန်ယက်စိုက်ပျိုးပါက တစ်ဧကလျှင် (၁၅)တင်းအထိ ထွက်နိုင်သည်။ မြန်မာနိုင်ငံတွင် (၆-၁၁)တင်းအထိ ထွက်သည်။

              </p>

              <p class="pt-3"><b>၆။ စိုက်ပျိုးခြင်း</b></p>
              <p>
                နှစ်ရှည်ပင် အမျိုးအစားဖြစ်ပြီး အများအားဖြင့် တစ်ရာသီခံပင် အဖြစ် အစိုက်များသည်။ ကိုင်းဖြာ(သို့) အပင်ပြန့် ပုံစံနှင့် တိုင်တွင် တွယ်တက်သည့် တိုင်ထောင်ဟူ၍ အပင်
                ပုံစံ နှစ်မျိုး ရှိသည်။ ကိုင်းဖြာ(သို့) အပင်ပြန့်မျိုး၏ အစေ့သည် အနည်းသေးပြီး အပင်အမြင့် ၂ပေခန့် မြင့်သည်။ တိုင်ထောင်မျိုးများထက် စောစွာ သီးပွင့်သည်။
                တိုင်ထောင် မျိုးများအတွက် မစိုက်ပျိုးမီ တိုင်များကို ဦးစွာထောင်ပါ။ မျိုးစေ့များကို မစိုက်ပျိုး မီရေစိမ်ခြင်းနှင့် စိုက်ပျိုးပြီး ရေများများ လောင်းခြင်းအား ရှောင်ရှားရမည်။
                စိုက်ပျိုးမည့် မြေကြီး၏ နံနက်ခင်းပိုင်း အပူချိန်သည် (၁၀) ဒီဂရီ စင်တီဂရိတ်ခန့် အနည်းဆုံး ရှိနေရမည်။
                ပင်ပြန့်မျိုးများကို ပင်ကြား (၁) ပေနှင့် တန်းကြား (၄) ပေ ထားပြီး တိုင်ထောင်မျိုးများအား ပင်ကြား (၈) လက်မနှင့် တန်းခြား (၂) ပေ ထား၍ စိုက်ပျိုးကြသည်။ မျိုးစေ့များကို
                မြေကြီးတွင် (၁) လက်မခွဲ (၂) လက်မ အနက်ခန့်တွင် စိုက်ပျိုးပါ။ (၁)ဧက စိုက်ပျိုးရန် အတွက် မျိုးစေ့ (၁၀)ပြည်မှ (၁) တင်းခန့် လိုအပ်မည် ဖြစ်သည်။

              </p>

              <p class="pt-3"><b>၇။ကျရောက်နိုင်သောရောဂါများ</b></p>
              <p>
                ထောပတ်ပဲပင်များတွင် ပင်စည်မှည့်ပေါက်စွန်းရောဂါများသည် ဆိုးရွားစွာ ကျရောက်တတ်သည်။ ထိုသို့ ကျရောက်ပါက ၎င်းစိုက်ခင်းတွင် ထောပတ်ပဲအား (၂)နှစ်ခန့် မစိုက်ဘဲ ထားရမည်။ ပျပိုးကောင်များကြောင့် အဝါရောင်မိုစေ့ ရောဂါများလည်း ကျရောက်တတ်သည်။ ခံနိုင်ရည်ရှိသော မျိုးများကို သာရွေးချယ် စိုက်ပျိုးခြင်း၊ သီးလှည့် စိုက်ပျိုးခြင်း တို့ကို ဆောင်ရွက်သင့်ပါသည်။
                စိုက်ခင်းအား ပျ၊ လှေးကျိုင်းများ၊ ထွာကောင်များ၊ ရွက်စားပိုးများ၊ သီးတောင့်ထိုးပိုးများ ကျရောက်တတ်သောကြောင့် အမှိုက်သရိုက်များ ကင်းရှင်းအောင် ဆောင်ရွက်ထားသင့်သည်။ အချို့ပိုးကောင်များအား ပြင်းအားကောင်းသောရေဖြင့် ထိုးချကာ နှိမ်နင်းနိုင်သည်။ တမာ ပိုးသတ်ဆေးနှင့် ဘော်ဒိုဆေးရည်များ အသုံးပြုကာ ကာကွယ်နိုင်သည်။

                သီးတောင့်တွင် အောက်ပါ အကြောင်းများကြောင့် အစေ့မတင်ခြင်းများ ဖြစ်ပေါ်သည်။
                ပိုးမွှားရောဂါများ (စိုက်ခင်းအား သန့်ရှင်းစွာ ထားရမည်။ ရောဂါ ရရှိသော အပင်များအား နုတ်ပြီး သေချာ စွန့်ပစ်ရမည်။)
                နိုက်ထရိုဂျင် အာဟာရ ဓာတ်လွန်ကဲခြင်း (ယူရီးယား ဓာတ်မြေသြဇာများ ထည့်သွင်းခြင်းအား ရှောင်ရှားပါ၊ မြေဆွေးများအား ဘောင်တင်၊ မြေတောင် မြှောက်သည့် ထည့်ပေးနိုင်ပါသည်။)
                နေ့ အပူချိန်နှင့် ည အပူချိန်များ လွန်ကဲစွာမြင့်တက်ခြင်း (စိုက်ခင်းအားကောက်ရိုး စသည်တို့နှင့် ဖုံးအုပ်ပေးရန် လိုအပ်ပါသည်။)
                ရေလွန်ကဲစွာ သွင်းခြင်း (ရေဆင်းကောင်းရန်နှင့် လိုအပ်မှရေသွင်းခြင်းတို့ ပြုလုပ်ပါ။)
              </p>

              <p class="pt-3"><b>၈။ရိတ်သိမ်းခြင်း</b></p>

              <p>
                ထောပတ်ပဲအား သီးတောင့်ထဲမှ အသီးကို ယူသောကြောင့် ကောင်းစွာခြောက်သွေ့ရန် လိုအပ်သည်။ သို့မဟုတ်ပါက ထောပတ်ပဲသည် သေးပြီး၊ နုမှည့်သဘော ဖြစ်နေမည်။
                ချက်ပြုတ်ဖို့လည်း ကောင်းမွန်မည် မဟုတ်ပါ။ ထို့ကြောင့် အလျင်စလို ခူးဆွတ်ခြင်း မလုပ်ဆောင်သင့်ပါ။ သီးတောင့်များ ဆူဖြိုး ကြီးထွားလာပြီး အဝါရောင် သို့ပြောင်းသွားသောအခါ
                (သို့) ခြောက်သွေ့ပြီး အညိုရောင်ပြောင်းသွားသောအခါ ခူးဆွတ် ရိတ်သိမ်းနိုင်သည်။ သို့သော် အစေ့များ ကွဲထွက်မသွားခင် ခူးဆွတ်ခြင်းဆောင်ရွက်ပါ။ အစေ့များအား အခြောက်လှန်းကာ
                အစိုဓါတ်ရာခိုင်နှုန်း (၁၄) အောက်တွင် ခြောက်သွေ့အေးမြပြီး အလင်းရောင် မရှိသောနေရာတွင် သိမ်းဆည်း ထားသင့်ပါသည်။
              </p>
