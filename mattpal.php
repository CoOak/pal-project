                တစ်ဧကနွားခြေ၊သဘာဝမြေဩဇာ(၁၀၀-၁၅၀)တင်း နေရာနှံအောင်ချပါ။ နွားထယ်(သို့)ထွန်းစက်ကြီးဖြင့် ထယ်တစ်စပ်(သို့)နှစ်စပ်ထိုးပါ။ ထယ်စာခဲခေါင်းဖွေးအောင် နှစ်နေသုံးနေလှမ်းပြီးနောက်မြေခံမြေဩဇာယူရီးယား(၈)ပြည်၊တီစူပါ(၂၄)ပြည်၊ပိုတက်(၁၂)ပြည်၊ဂျစ်ပဆမ်(၂၀)ပြည်ကို ရောမွှေကြဲပတ်ပါ။ ထို့နောက် ထွန်းရေသင့်တင့်စွာ ညက်အောင်ထွန်းနှစ်စပ်မွှေပေးပါ။ မြေကောင်းမြေမွသောဒေသများတွင် စပါးရိုးပြတ်တိုအောင်ရိတ်၍ ပထမဦးစွာ မတ်ပဲမျိုးစေ့ကြဲးပတ်ပြီး ကွန်းဒေါင်းထွန်း(Rotavator)နှင့်် နှစ်စပ်မောင်း၍ ကြမ်းတုန်းရိုက်ပြီး စိုက်ပျိုးရပါမည်။ မြေစေးမြေမာသောစိုက်ခင်းများတွင် ပထမဦးစွာနွားထယ်(သို့)ထွန်စက်ကြီးဖြင့် ထယ်တစ်စပ်မောင်း၍ ထွန်တစ်စပ်ခြေပြီး မျိုးစေ့ကြဲပတ်ကာ နွားထွန်(သို့)ကွန်းဒေါင်းထွန်ဖြင့် တစ်စပ်မွှေ၍ ကြမ်းတုန်းရိုက်စိုက်ပျိုးရပါမည်။ ထယ်ထိုးခြင်း၊ထွန်းမွှေခြင်းများကို မြေ၏အခြေအနေပေါ်မူတည်၍ လိုအပ်သလို ဆောင်ရွက်ရပါမည်။
                မျိုးအဖြစ်ရေဆင်းစိုက်ပျိုးရေးသုတေသနမှထောက်ခံထုတ်ဝေထားသည့် ရေဆင်း(၂)၊ ရေဆင်း(၄)၊ ရေဆင်း(၆)၊ ပုလဲထွန်း စသည့်မျိုးကောင်းမျိုးသန့်များ(သို့) မိမိဒေသတွင် စိုက်ပျိုးဖြစ်ထွန်းနေသော မျိုးများရှိပါက မျိုးသန့်အဖြစ်ဆောင်ရွက်၍ မျိုးအဖြစ် စိုက်ပျိုးနိုင်ရေးကို ဆောင်ရွက်ပေးရန်လိုအပ်ပါသည်၊ တစ်ဧကရိတ်သိမ်းအပင်(၁၃၀၀၀၀)ရရှိရန် အတန်းလိုက်စိုက်ပါက(၆-၈)ပြည်နှုန်းသုံးပါ။ ကြဲပတ်ပါကမျိုးစေ့(၁၂)ပြည်နှုန်းသုံးပါ။


              </p>

              <p class="pt-3"><b>၇။ စိုက်ပျိုးခြင်း</b></p>

              <p>
                (၀၁ ရက်) <br>

                မတ်ပဲမျိုးစေ့များကို နာရီဝက်ခန့်ရေစိမ်ပြီး ပဲမြစ်ဖုဇီဝမြေဩဇာတစ်ထုတ်နှင့် သမအောင်ရောမွှေစိုက်ပါ၊။ အပင်အစိပ်ကျဲညီအောင် မျိုးစေ့ကို နှစ်လွှာကြဲပါ။ ကြဲပြီးပြီးချင်းကြမ်းဖြင့် နှစ်ပြန်အုပ်ပေးပါက အစိုဓာတ်လုံလောက်စွာ ရရှိ၍ အပင်ပေါက်ညီညာပြီး လိုအပ်သော အပင်ဦးရေရရှိနိုင်ပါသည်၊ အတန်းလိုက်စိုက်မည်ဆိုပါက စိုက်ကြောင်းပေး၊မျိုးစေ့ချပြီး ကြမ်းပြန်အုပ်ပေးရပါမည်။ ကြဲပတ်မည်ဆိုပါက ညနေပိုင်းတွင် ကြဲပတ်ရန် သင့်တော်ပြီး လက်ဆညီအောင်ကြဲပတ်ရန် လိုအပ်ပါသည်။ အပင်ညိုးသေနှင့် အခြားရောဂါများ မကျရောက်အောင် မျိူးစေ့ကို မှိုသတ်ဆေးတစ်မျိုးမျိုးဖြင့်လူးနယ်၍ စိုက်ပျိုးပါ။ အပင်ပေါက်ညီညာ၍ အပင်ပေါက်ကောင်းအောင် ထယ်ရေးအတွင်း မျိုးစေ့ကို အတိမ်အနက်(၁.၂) လက်မအတွင်း ရအောင်စိုက်ပါ။ အပင်စပေါက်သည်၊၊<br>

                (၀၄ ရက်)<br>

                -အပင်ပေါက်စုံပါသည်။<br>

                (၁၀ ရက် -၁၅ ရက်)<br>

                အပင်ငယ်စဉ် လှေး၊ကျိုင်း၊ ပျ၊မွှား၊ပင့်ကူနီ၊ပင်စည်ထိုးယင်တို့ ကာကွယ်ရန် ပင်လုံးပြန့် သတ္တိရှိသော ဆေးတစ်မျိုးမျိုးကို တစ်ဧကလျှင် သတ်မှတ်နှုန်းဖျန်းပါ။ ညိုးသေရောဂါ ကာကွယ်ရန်မှိုသတ်ဆေးတစ်မျိုးမျိုးဖျန်းပါ။ ပေါင်းမြက်ကင်းစင်အောင် စတင်ဆောင်ရွက်ပါ။<br>

                (၁၅ရက်-၂၅ရက်) <br>

                ပဲပင်များ ကြီးထွားသန်မာဖြစ်ထွန်းရန် (NPK)နှင့် အနည်းလိုအာဟာရဓာတ်များ ပါဝင်သော ရွက်ဖြန်းမြေဩဇာများတွင် နိုက်ထရိုဂျင် အချိုးများသည့် သင့်တော်သော ရွက်ဖြန်းမြေဩဇာတစ်မျိုးမျိုးကို(၁၅)ရက် ခြား၍ နှစ်ကြိမ်ပတ်ဖျန်းပါ။ ပေါင်းမြက်ကင်းစင်အောင် ဆောင်ရွက်ပါ။ သီးကိုင်းဖူးလေးမျာ းစတင်နိုးကြားလာသည်။ ထိုအချိန်တွင် အစိုဓာတ်မပျက်ရန်၊ အာဟာရဓာတ်မချို့တဲ့ရန် ၊ပိုးမွှားရောဂါ မကျရောက်ရန် ဂရုပြုရမည်။<br>

                (၃၀ရက်-၃၅ရက်)<br>

                (၃၅)ရက်ခန့်တွင် ကိုင်းဖျားပန်းဖူးလေးများ ဖြစ်ပေါ်လာသည်။ ပန်းဖူးပေါ်ပြီး တစ်ပတ်အကြာ ပန်းစပွင့်သည်။ (၄၅)ရက်သားမှ (၆၀)ရက်သားအတွင်း ပန်းဖြိုင်ဖြိုင်ပွင့်၍ ပွင့်အားကောင်းအသီးတောင့်များရန်(NPK)အချိုးပါသော ရွက်ဖြန်းမြေဩဇာများတွင် ဖော့စဖရပ်နှင့် ပိုတက်ဆီရမ်ရာခိုင်နှုန်းများသော သီးပွင့်ရွက်ဖျန်းမြေဩဇာများကို (၁၅)ရက်ခြား နှစ်ကြိမ်ဖျန်းပါ။ (သို့)ရေတစ်ဂါလံတွင် ယူရီးယား(၄.၅)ဇွန်းနှင့် လက်ချားတစ်ဇွန်းရော၍ တစ်ဧကလျှင် ဂါလံ(၂၀)ဖျန်းပါ။<br>

                (၃၅ရက်-၆၀ရက်)<br>

                ရွက်ဖောက်၊လှေး၊ကျိုင်း၊ရွက်စားငမြောင်တောင်၊ပင်စည်ထိုးယင်၊ပျပိုး၊သရစ်၊ထွာကောင်စိမ်း၊ရွက်စုံစားခူဝါ၊ပွင့်ထွေးရွက်ကပ်သီးလုံးဖောက်ပိုးစသည်တို့နှင့်ယင်ဖြူကြောင့်ဖြစ်သောအဝါရောင်မှိုစိတ်ဗိုင်းရက်စ်ရောဂါများမကျရောက်စေရန်ဖြစ်ပါသည်။(၆၀)ရက်သားအထိ(၁၅)ရက်တစ်ကြိမ်ပင်လုံးပြန့်ပိုးသတ်ဆေးတစ်မျိုးမျိုးကိုအလှည့်ကျပတ်ဖျန်းပါ။ <br>

                (၁၅ရက်-၆၀ရက်)<br>

                သီးနှံသက်တမ်းတလျှောက်လုံး ပင်ညှိုးရောဂါ၊ ဖားဥမှိုရောဂါ၊ သံချေးရောဂါ၊ ဆာကိုစပိုးရားရွက်ပြောက်ရောဂါ၊ မှည့်ပြောက်စွန်းရောဂါ၊ ဗိုင်းရပ်စ်မိုစိတ်ရောဂါစသည့် ရောဂါများ ကျရောက်တက်၍ မှိုသတ်ဆေးများမှ ရောဂါနှင့်သင့်တော်သည့် ဆေးတစ်မျိုးမျိုးကို (၁၅)ရက်တစ်ကြိမ် အလှည့်ကျ(၄)ကြိမ်ဖျန်းပါ။ အစေ့အဆံတည်ချိန်ဖြစ်၍ သီးလုံးဖောက်ပိုးနှင့် ထိပ်ပုပ်ရောဂါကိုဂရုပြုပါ။ ပန်းပွင့်သန္ဓေအောင်ပြီး ပဲတောင့်ကလေးများ ဖြစ်ပေါ်လာသည်။<br>

                (၆၀ရက်-၇၀ရက်)<br>

                တဖြည်းပဲတောင့်အတွင်း ပဲစေ့လေးများ ဖြစ်ပေါ်လာသည်။ ပဲစေ့လေးများ ပြည့်တင်းကြီးထွားချိန်ဖြစ်၍ တစ်ပင်လုံးချက်လုပ်သော အစာ၏(၆၀)%ကိုအစေ့အောင်မြင်ရန် အသုံးချရ၍ အစာအလိုအပ်ဆုံးဖြစ်ပါသည်။ ထိုအချိန်တွင် အစာချက်နှုန်းကောင်းရန် အရွက်များစိမ်းစိုကျန်းမာနေရန် အရေးကြီးသည်။ အာဟာရနှင့် အစိုဓာတ်မပျက်ရန်အရေး ကြီးသည်။<br></p>

              <p class="pt-3"><b>၈။ကျရောက်နိုင်သောရောဂါများ</b></p>
              <p>
                အဝါရောင် မိုစိတ်ဗိုင်းရပ်စ်ရောဂါ

                စာကိုစပိုးရားရွက်ပြောက်ရောဂါ (သံချေးရောဂါ)၊

                အန်းသရနို့စ် (မှဲ့ပြောက်စွန်းရောဂါ)၊
                ပေါင်ဒရီမစ်ဂျူး (ဖားဥမှို)၊

                ဘလိုက် (ရွက်ခြောက်လောင်)၊

                ဟေလိုဘလိုက် (ရွက်ခြောက်ကိုင်းခြောက်)၊

                ရက်စ် (သံချေးရောဂါ)၊

                ဖြူဆေရီယမ် (မှိုညှိုးသေရောဂါ)၊

                ပိုင်သီရမ်မှိုညှိုးသေရောဂါ၊

                မြစ်ခြောက်ဆွေး စခရိုရန်၊

                ပင်ခြေမှိုဖြူစွဲ ညှိုးသေရောဂါ၊

                မြစ်ဖုနီမတုတ် အမြစ်တွင်ဖြစ်သော အခြားနီမတုတ်များ စတဲ့

                ရောဂါတွေကျရောက်တတ်ပြီးတော့ အကုန်လုံးတော့ မကျရောက်ပါဘူး။
              </p>

              <p class="pt-3"><b>၉။ ရိတ်သိမ်းခြင်း</b></p>
              <p>
                (၇၅ရက်-၁၁၀ရက်)<br>

                မတ်ပဲမျိုးနှင့် ပတ်ဝန်းကျင်ရာသီဉတု ထိမ်းသိမ်းစောင့်ရှောက်မှုအပေါ်မူတည်၍ (၇၅-၁၁၀)ရက်အတွင်း ရင့်မှည့်လေ့ရှိကြသည်။ အရွက်များဝါခြောက်ပြီး အတောင့်များ၏(၇၅-၈၅)%အနက်ရောင်သို့ပြောင်း၍ ခြောက်သွေ့ချိန်တွင် ရိတ်သိမ်းပါ။ ရိတ်သိမ်းပြီး နှစ်နေသုံးနေလှမ်း၍ ချွေလှေ့သန့်စင်ပါ။ နေလှမ်းပြီးအစိုဓာတ်(၈-၉)% ရှိချိန်တွင် သိုလှောင်ပါ။

              </p>




