                သဘာဝမြေသြဇာများ သို့မဟုတ် အီးအမ္ဘိုကာရွိကို (၇-၁၀) အိတ်ကို စိုက်ကြောင်းထဲသို့ ထည့်ပေးပါ။ အကယ်၍ ဓာတ်မြေသြဇာ အသုံးပြုလိုပါက တစ်ဧကလျှင် ပုလဲ(၂၈-၅၆ပေါင် သို့ ၈ပြည်- ၁၅ပြည်)၊ တီစူပါ (၈၄-၁၁၂ပေါင် သို့ ၂၃ပြည်-၃၁ပြည်)၊ ပိုတက် (၃၈ ပေါင် သို့ ၂၃ ပြည်) ကို စိုက်ကြောင်းအတွင်း လိုက်ဖြူးထည့်ပေးပါ (သို့မဟုတ်) ကွန်ပေါင်း မြေသြဇာတစ်အိတ်ကို ထည့်ပေးပါ။

              </p>

