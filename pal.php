                ညှပ်၍စိုက်ပျိုးကြသည်။သက်ကြီးမျိုးကို
                တန်းကြား(၄.၅)ပေ၊ပင်ကြား(၁)ပေ၊သက်လတ်မျိုးကိုတန်းကြား(၃)ပေ၊ပင်ကြား(၁)ပေ၊
                သက်လျှင်မျိုးကိုတန်းကြား(၂)ပေပင်ကြား(၆)လက်မ
                စိုက်ပျိုးရသည်။ပဲစင်းငုံကို သီးညှပ်စိုက်ပျိုးရာတွင်
                ပင်ကြားတန်းကြားကို (၁.၅-၃)ပေ (၇-၁၀.၅)
                ပေထိထား၍စိုက်ပျိုးလေ့ရှိပါသည်။ ပဲစင်းငုံသီးနှံတစ်ဧက(၂၅) တင်း
                ထွက်ရှိရန်အတွက် ဆောင်ရွက်ရမည့် လုပ်ငန်းအဆင့်ဆင့်
              </p>

              <p class="pt-3"><b>၆။ ကြိုတင်ပြင်ဆင်ခြင်း</b></p>
              <p>
                တစ်ဧက သဘာဝမြေဩဇာနွားချေး (၁၀၀-၁၅၀)တင်းရော၍
                နေရာနှံ့အောင်ချပါ။နွားထယ် (သို့)ထွန်စက်ကြီးနှင့်
                တစ်စပ်ကမ်းပြိုအောင် အနည်းဆုံးအနက်(၆)လက်မ ရအောင်ထိုးပြီး
                ယူရီးယား(၈)ပြည်၊ တီဆူပါ(၂၄)ပြည်၊ပိုတက်(၁၂)ပြည်၊
                ဂျစ်ပဆန်(၂၀)ပြည်ကို သမအောင်ရောမွှေပြီး
                မြေခံကျဲပတ်ပါ။ထွန်းရေးကောင်းစွာ ညက်အောင်သုံးစပ်မွှေပါ။
                ရေဝပ်ခြင်းကင်းဝေရန်မြေမျက်နှာပြင် ညီညာစွာညှိပေးပါ။အပင်ငယ်စဉ်
                ရေဝပ်ပါက တစ်ညအိပ်ပင်မခံနိုင်ပါ။ မျိုးအဖြစ် မုံရွာရွှေဒင်္ဂါး
                ၊သူဌေးကန်၊ ရေဆင်း(၆)၊ ရေဆင်း(၇)မျိုးများကို
                အထွက်ကောင်းမျိုးများ အသုံးပြုပါ။မြေတွင်းအောင်းပိုးများ
                ကာကွယ်ရန်
                ဖျူရာဒန်(၁-၃)ပိဿာ/ဧကစိုက်ပျိုးမည့်မျိုးစေ့ဖြင့်လူးနယ်စိုက်ပျိုးပါ။
              </p>

              <p class="pt-3"><b>၇။ စိုက်ပျိုးခြင်း</b></p>

              <p>
                ( ၀၁ ရက်)
                အပင်ပေါက်ကောင်း၍အပင်ပေါက်ညီညာရန်ပဲစင်းငုံမျိုးစေ့များကိုနာရီဝက်ခန့်ရေစိမ်ပြီးပဲမြစ်ဖုဇီဝမြေဩဇာတစ်ထုတ်နှင့်သ
                မအောင်ရောမွှေပြီးမှစိုက်ပါ။အပင်ငယ်စဉ်ညိုးသေရောဂါကျတက်သောကြောင့်မှိုသတ်ဆေးတစ်မျိုးမျိုးကိုမျိုးစေ့တစ်ပြည်လျှင်(၁)ဂရမ်နှုန်းဖြင့်
                လူးနယ်စိုက်ပါ၊ သက်ကြီးမျိုးအတွက်
                တစ်ဧကအပင်ဦးရေ(၉၆၀၀)ပင်၊သက်လတ်မျိုးအား တစ်ဧက (၁၄၅၂၀)ပင်၊
                သက်လျှင်တစ်ဧက (၄၃၅၆၀)ပင်၊ဝင်
                ဆန့်ရန်အတွက်မျိုးစေ့နှုန်း(၄-၆)ပြည်နှုန်းသုံးပါ။အပင်ပေါက်ညီညာ၍အ
                ပင်ပေါက်ကောင်းရန်ထယ်ရေးအတွင်းမျိုးစေ့အတိမ်အနက်(၁-၂)လက်အတွင်းရအောင်စိုက်ပါ။
                (၀၄ ရက်) အစေ့မှ စတင်အပင်ပေါက်သည်။ (၁၅ရက်)
                အပင်ပေါက်စုံသည်။ပင်ညိုးရောဂါကာကွယ်ရန်မှုသတ်ဆေးတစ်မျိုးမျိုးပတ်ဖျန်းပါ။အပင်ငယ်စဉ်လှေး၊ကျိုင်း၊မွှား၊ပင်ကူးနီစသည့်ပိုးများကျတက်သောကြောင့်ပင်လုံးပြန့်သတ္တိရှိသည့်ဆေးတစ်မျိုးမျိုးပတ်ဖျန်းပါ။(မွှာ၊ပင်ကူးသည်ပင်ဖိုမှိုစိတ်ရောဂါကိုသယ်ဆောင်သောပိုးဖြစ်သည်။
                (၁၅ရက်-၅ရက်)
                ပဲပင်များကြီးထွားသန်မာဖြစ်ထွန်းရန်(NPK)နှင့်အနည်းလိုအာဟာရဓာတ်များပါဝင်သောရွက်ဖျန်းမြေဩဇာများတွင်နိုက်ထရိုဂျင်အချိုးများသည့်သင့်တော်သောရွက်ဖျန်းမြေဩဇာတစ်မျိုးမျိုးကို(၁၅)ရက်ခြားတစ်ကြိမ်ဖြင့်(၃)ကြိမ်ပတ်ဖျန်းပါ။
                (၁၅ရက်-၆၀ရက်) ရွက်ဖောက်၊ လှေး၊ ကျိင်း၊ ရွက်စားပိုး၊
                ငမြောင်တောင် ၊ပျပိုး၊ သရစ် ၊လှေးပိုး၊ ပွင့်ထွေး၍ရွက်ကပ်ပိုး
                ၊မွှာပင့်ကူးနီ စသည့်ပိုးများမကျရောက်စေရန် (၁၅-၆၀)ရက်သားအထိ
                ပင်လုံးပြန့်ပိုးသတ်ဆေးတစ်မျိုးမျိုး(သို့)
                အခြားထိသေဆေးတစ်မျိုးမျိုးဖြင့် ပတ်ဖျန်းပါ။
                သီးနှံသက်တမ်းတလျောက် လုံးပင်ညိုးရောဂါ ၊ဖားဥမှိုရောဂါ၊
                သံချေးမှိုရောဂါ၊ ရွက်ခြောက်ရောဂါ ကျရောက်တက်၍ သင့်တော်သော
                ပိုးသတ်ဆေးတစ်မျိုးမျိုးကို (၁၅)ရက်ခြားတစ်ကြိမ် (၄)
                ကြိမ်ပတ်ဖျန်း ပါ။ (၂၀ရက်-၆၀ရက်)
                ပေါင်းမြက်ကင်းစင်ရန်(၂၀)ရက်သားလျှင်ကြားထွန်းလိုက်ပါ။(၂၅)ရက်သားတွင်သံထွန်းလိုက်ပါ။(၃၀)ရက်သားတွင်ပေါက်
                ပြားပေါင်းလိုက်ပါ။(၆၀)ရက်သားတွင်ပေါင်းမြက်ကင်းစင်ရန်နှင့်ရရှိထားသောမိုးရေနှင့်အစိုဓာတ်ကိုထိန်းသိမ်းနိုင်ရန်ထယ်
                ကြောင်းထိုးပါ။ပန်းပွင့်ချိန်တွင်ပွင့်အားကောင်း၍သီးတောင့်များရန်(NPK)အချိုးမြေဩဇာများတွင်(P)နှင့်(K)အချိုးများသောသီးပွင့်ရွက်မြေဩဇာ(၁၀)ရက်ခြား(၂)ကြိမ်ဖျန်းပါ။(သို့)ယူရီးယား(၄.၅)ဇွန်းနှင့်လက်ချား(၁)ဇွန်း၊ရေတစ်ဂါလံနှင့်ရောပြီးတစ်ဧက(၂၀)ဂါလံနှုန်း
                ဖျန်းပါ။ (၈၀ရက်-၉၀ရက်) တစ်တောင့်ပါအောင်စေ့အရေအတွက်
                သတ်မှတ်ချက်အတိုင်း ရရှိရန်လိုအပ်ပါသည်။ တစ်တောင့်လျှင်
                အောင်စေ့(၃-၄)စေ့ပါရှိရန် သီးတောင့်ဖြစ်ချိန်၊
                အစေ့အဆံတည်ချိန်ဖြစ်၍ သီးလုံးဖောက်ပိုး
                ဂရုစိုက်ပါ။ပင်လုံးပြန့်သတ္တိရှိသော ဆေးတစ်မျိုးမျိုးနှင့်
                ထိသေအာနိသင်ရှိဆေးတစ်မျိုးမျိုးအား တစ်လှည့်စီဖျန်းပါ။
                အစေ့အဆံဖြစ်ထွန်းချိန်တွင် (NPK)
                အချိုးရွက်ဖျန်းမြေဩဇာများတွင်
                (P)နှင့်(K)အချိုးများသည့်ရွက်ဖျန်းမြေဩဇာတစ်မျိုးမျိုးပတ်ဖျန်းပါ။
                ရိတ်သိမ်းခြင်း (၁၀၅ရက်-၁၄၀ရက်)
                အတောင့်များ(၇၅-၈၅)%ရင့်မှည့်ချိန်တွင်ဆွတ်ခူးရိတ်သိမ်းပါ။(၇-၁၄)ရက်အထိနေလှန်သန့်စင်၍အစိုဓာတ်(၈-၁၀)%
                အောက်ထားရှိပြီးသိုလှောင်ပါ။သိုလှောင်ရာတွင်အစေ့ထိုးပိုးဝင်ရောက်တက်သဖြင့်ဆေးနှင့်လူးနယ်သိုလှောင်ပါ။
              </p>
